func parseSecurityQuestionSettingCompositeId(compositeId string) (securityQuestionSettingId string, err error) {
	parts := strings.Split(compositeId, "/")
	match, _ := regexp.MatchString("idcsEndpoint/.*/securityQuestionSettings/.*", compositeId)
	if !match || len(parts) != 4 {
		err = fmt.Errorf("illegal compositeId %s encountered", compositeId)
		return
	}

	securityQuestionSettingId, _ = url.PathUnescape(parts[3])

	return
}

